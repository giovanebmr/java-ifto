    private void botaoCadastrar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrar
        try {
            JogadorDao dao = new JogadorDao();
            Jogador jogador = new Jogador(nome.getText(), login.getText(), email.getText(), DigestUtils.sha512Hex(new String(senha.getPassword())));
            dao.addJogador(jogador);           
            JOptionPane.showMessageDialog(rootPane, "Cadastro realizado!", "Aviso", 1);          
            dispose();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(CadastroDeJogador.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Falha no cadastro!", "Aviso", 0);
        }
    }//GEN-LAST:event_botaoCadastrar

