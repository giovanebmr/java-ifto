public class calculos {
    
    int resultado;
    
    public int soma(int x, int y){
        resultado = x + y;
        return resultado;
    }
    
    public int subtracao(int x, int y){
        resultado = x - y;
        return resultado;
    }
    
    public int multiplicacao(int x, int y){
        resultado = x * y;
        return resultado;
    }
    
    public int divisao(int x, int y){
        resultado = x / y;
        return resultado;
    }
    
}
